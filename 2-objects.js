/*
  Write a JavaScript function to list the properties of a JavaScript object
  Sample object:
  const student = {
    name : "David Rayy",
    sclass : "VI",
    rollno : 12
  };
  Sample Output: "name,sclass,rollno"
*/
function getObjectKeys(obj) {
  // Your code goes here ...
  return Object.keys(obj).join(',')
}

console.log(getObjectKeys({ name: 'David Ravy', sclass: 'VI', rollno: 12 })) // => "name,sclass,rollno"

/*
  Write a JavaScript function to retrieve all the values of an object's properties
  Sample object:
  const party = {
    date: '01/01/2020',
    place: 'Home',
    participants: ['Jake', 'Paul', 'Julia']
  };
  Sample Output: ['01/01/2020', 'Home', ['Jake', 'Paul', 'Julia']]
*/
function getObjectValues(obj) {
  // Your code goes here ...
  return Object.values(obj)
}

console.log(getObjectValues({ date: '01/01/2020', place: 'Home', participants: ['Jake', 'Paul', 'Julia'] }));